            <p class="about_head">Madras Naturalists&apos; Society</p>
            <p class="about_p">Madras Naturalists&apos; Society (MNS) is a non-governmental organisation working for conservation of nature and creation of environmental awareness. MNS was formed when a small group of like-minded naturalists came together in Chennai in 1978. Since then, MNS has gone about achieving its objectives in various ways. MNS organises regular field trips to nature-rich areas, conducts and participates in wildlife surveys, collaborates with other organisations with similar objectives and works towards preserving habitats of wildlife significance. MNS conducts nature education programmes for schools in both cities and rural areas. In 2013 MNS instituted a nature writing award in memory of Mr. M. Krishnan, the renowned naturalist writer and photographer.</p>
            <p class="about_p">Monthly meetings of MNS are held at Chennai.</p>
            <p class="about_head">Blackbuck </p>
            <p class="about_p">MNS has been publishing a journal, <i>Blackbuck</i>, since 1985. <i>Blackbuck</i> is a unique publication. It carries both semi-technical articles and works that are of popular interest. These include original observations on nature, predominantly from southern India, book reviews, miscellaneous notes, and suchlike.</p>
            <p class="about_p">Before publication of <i>Blackbuck</i> commenced, such pieces of writing appeared in the monthly bulletin of the MNS. Full-fledged articles and field observations of members continue to be published in the bulletin, which also documents the monthly activities of the society.</p>
            <p class="about_p">Selected articles that appeared in <i>Blackbuck</i> were published in the book <i>Sprint of the Blackbuck</i>, which was edited by the award-winning author Mr. Theodore Baskaran.</p>
            <p class="about_p">P.M. Rangarajan, V. Santharam, V.J. Rajan, A. Rajaram, K.V. Sudhakar and Kumaran Sathasivam have served as honorary editors of <i>Blackbuck</i> over the years.</p>
            <p class="about_head">This CD</p>
            <p class="about_p">This CD contains all the articles that appeared in the first 25 volumes of <i>Blackbuck</i>. It also contains in digital form the issues of the bulletin from 1978 to 2010.</p>
            <p class="about_p">A powerful search facility has been provided in this CD.</p>
            <p class="about_p">This CD will be of interest to a wide audience, ranging from professional scientists and amateur naturalists to the general public.</p>
